void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	if (nb < 0)
	{
		nb *= -1;
		ft_putchar('-');
	}
	else if (nb < 10)
	{
		ft_putchar(nb + 48);
	}
}
