#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	if (ac != 2)
	{
		write(1, "\n", 1);
		return (0);
	}
	i = 0;
	while (av[1][i] != '\0')
	{
		if (av[1][i] >= 'A' && av[1][i] <= 'Z')
		{
			if (av[1][i] >= 'N')
				av[1][i] = av[1][i] - 13;
			else
				av[1][i] = av[1][i] + 13;
		}
		if (av[1][i] >= 'a' && av[1][i] <= 'z')
		{
			if (av[1][i] >= 'n')
				av[1][i] = av[1][i] - 13;
			else
				av[1][i] = av[1][i] + 13;
		}
		write(1, &av[1][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
